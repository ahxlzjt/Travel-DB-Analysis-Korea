CREATE VIEW DB2023_cheap_eating AS
SELECT name, description
FROM DB2023_eating
WHERE eating_id IN (
  SELECT eating_id
  FROM DB2023_menu
  GROUP BY eating_id
  HAVING AVG(cost) <= 15000);

